const USER_ROLES = {
    Admin: 'Administrator', Staff: 'Staff'
}

function User ({ id, name, email, role, pass, salt, emailToken, emailTokenTime }) {
    this.id = id ?? 0;
    this.name = name;
    this.email = email;
    this.role = role ?? USER_ROLES.Staff;
    this.pass = pass;
    this.salt = salt;
    this.emailToken = emailToken ?? '';
    this.emailTokenTime = emailTokenTime ?? '';
}

module.exports = { User, USER_ROLES }